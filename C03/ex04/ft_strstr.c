char	*ft_strstr(char *str, char *to_find)
{
	char	*s1;
	char	*s2;

	while (*str)
	{
		s1 = str;
		s2 = to_find;
		while (*s1 && *s2 && (*s1 == *s2))
		{
			s1++;
			s2++;
		}
		if (!*s2)
			return (str);
		str++;
	}
	return ((void *) 0);
}
