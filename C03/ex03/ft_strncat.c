char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	char	*p;

	p = dest;
	while (*p != '\0')
		p++;
	while (*src != '\0' && nb-- > 0)
	{
		*p = *src;
		p++;
		src++;
	}
	*p = '\0';
	return (dest);
}
