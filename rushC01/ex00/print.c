#include "lib.h"

void	print_matrix(int ***matrix)
{
	int	i;
	int	j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			ft_putchar(matrix[1][i][j] + 48);
			if (j != 3)
				ft_putchar(' ');
			if (j == 3)
				ft_putchar('\n');
			j++;
		}
		i++;
	}
}
